// Tipos Primitivos
//Number
var x = 10;
console.log(typeof x, x, x.toPrecision(2));
var y = 5.890;
console.log(y, y.toFixed(3));
//String
var firstName = 'Carlos';
var lastName = 'Eduardo';
var fullname;
fullname = firstName + " " + lastName;
console.log(fullname);
console.log(fullname.toUpperCase());
//Boolean
var a = false;
console.log(a, typeof a);
a = true;
console.log(a);
